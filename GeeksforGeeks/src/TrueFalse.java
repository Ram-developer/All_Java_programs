public class TrueFalse {
    public static void main(String[] args) {
        boolean a=true,b=false;
        if (a&&b){
            System.out.println("true");
        } else {
            System.out.println("false");
        }
        if (a||b){
            System.out.println("true");
        } else {
            System.out.println("false");
        }

        if ((!b) && (!b) ){
            System.out.println("true");
        } else {
            System.out.println("false");
        }
    }
}
